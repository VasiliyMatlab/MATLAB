clear;

x = [1 2.2 3.7 6.4 9 11.5 14.2 17.8 20.5 23.2];
y = [12 9 6.6 5.5 7.2 9.2 9.6 8.5 6.5 2.2];
p1 = polyfit(x,y,1);
p2 = polyfit(x,y,2);
p3 = polyfit(x,y,3);
p4 = polyfit(x,y,4);
p5 = polyfit(x,y,5);
p6 = polyfit(x,y,6);
xc = linspace(x(1),x(end),1000);
yc1 = polyval(p1,xc);
yc2 = polyval(p2,xc);
yc3 = polyval(p3,xc);
yc4 = polyval(p4,xc);
yc5 = polyval(p5,xc);
yc6 = polyval(p6,xc);

subplot(3,2,1);
plot(x,y,'ok',xc,yc1,'-k');
grid on; title('n = 1');
xlabel('x'); ylabel('y');
subplot(3,2,2);
plot(x,y,'ok',xc,yc2,'-k');
grid on; title('n = 2');
xlabel('x'); ylabel('y');
subplot(3,2,3);
plot(x,y,'ok',xc,yc3,'-k');
grid on; title('n = 3');
xlabel('x'); ylabel('y');
subplot(3,2,4);
plot(x,y,'ok',xc,yc4,'-k');
grid on; title('n = 4');
xlabel('x'); ylabel('y');
subplot(3,2,5);
plot(x,y,'ok',xc,yc5,'-k');
grid on; title('n = 5');
xlabel('x'); ylabel('y');
subplot(3,2,6);
plot(x,y,'ok',xc,yc6,'-k');
grid on; title('n = 6');
xlabel('x'); ylabel('y');